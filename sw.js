// Push notification handler
self.addEventListener('push', (event) => {
  const data = event.data ? event.data.json() : {};
  const title = data.title || 'GS Quant Alert';
  const options = {
    body: data.body || 'New stock alert',
    icon: 'data:image/svg+xml,<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 100 100"><rect width="100" height="100" rx="20" fill="%23080c15"/><text x="50" y="65" font-size="50" text-anchor="middle" fill="%2300e5ff">⚡</text></svg>',
    badge: 'data:image/svg+xml,<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 100 100"><rect width="100" height="100" rx="20" fill="%2300e5ff"/></svg>',
    vibrate: [100, 50, 100],
    data: data.data || {},
    actions: [
      { action: 'view', title: '📊 View' },
      { action: 'dismiss', title: '✖️ Dismiss' }
    ]
  };

  event.waitUntil(
    self.registration.showNotification(title, options)
  );
});

// Notification click handler
self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  if (event.action === 'view') {
    event.waitUntil(
      clients.openWindow('/')
    );
  }
});